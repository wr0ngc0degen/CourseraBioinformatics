public class SymbolEdge
{
    private String from;
    private String to;

    public SymbolEdge(String from, String to)
    {
        this.from = from;
        this.to = to;
    }

    public String getFrom()
    {
        return from;
    }

    public String getTo()
    {
        return to;
    }
}
